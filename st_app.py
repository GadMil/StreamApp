def check_password():
    """Returns `True` if the user entered the correct password."""
    def password_entered():
        if st.session_state["password"] == st.secrets["PASSWORD"]:
            st.session_state["password_correct"] = True
            del st.session_state["password"]  # don't store the password
        else:
            st.session_state["password_correct"] = False

    if "password_correct" not in st.session_state:
        st.text_input(
            "Enter password", type="password", on_change=password_entered, key="password"
        )
        return False
    elif not st.session_state["password_correct"]:
        st.text_input(
            "Enter password", type="password", on_change=password_entered, key="password"
        )
        st.error("‚ùå Password incorrect")
        return False
    else:
        return True


if not check_password():
    st.stop()

st.write("‚úÖ Access granted!")


def show_price_chart(prices, days):
    if not prices or len(prices) < 2:
        st.write("No chart available.")
        return

    fig = go.Figure()
    fig.add_trace(go.Scatter(
        y=prices[-days:],
        x=list(range(len(prices[-days:]))),
        mode="lines",
        line=dict(width=2, color="green" if prices[-1] >= prices[0] else "red"),
        showlegend=False
    ))
    fig.update_layout(
        height=250,
        margin=dict(l=20, r=20, t=20, b=20),
        xaxis=dict(title="Days", showgrid=False),
        yaxis=dict(title="Price", showgrid=True),
        plot_bgcolor="white",
        paper_bgcolor="white"
    )
    st.plotly_chart(fig, use_container_width=True)



